let variable = 17;


//Wenn die Zahl kleiner ist als 20 gib aus "Mini"

if (variable < 20) {
    console.log(variable + " ist gleich Mini")
}



//Wenn die Zahl zwischen 20 und 50 ist gib aus "Medium"

if (variable >= 20 && variable <= 50) {
    console.log(variable + " ist gleich Medium")
} else {
    console.log(variable + " ist nicht Medium")
}


//Wen die Zahl größer als 50 ist gib aus "Large"

if (variable > 50) {
    console.log(variable + " ist gleich Large")
} else {
    console.log(variable + " ist nicht Large")
}




